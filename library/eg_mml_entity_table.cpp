QHash< QString, QString> EgMMLEntityTable::initNameLookup(void)
{
    QHash< QString, QString> hash;

    for (QHash< QString, QString>::iterator i = valueLookup.begin(); i != valueLookup.end(); i++) {
        hash.insert(mmlDecodeEntityValue( i.value()) , i.key() );
    }

    return hash;
}

